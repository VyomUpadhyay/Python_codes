City = str(input("Enter the city Name => "))

if City=='Ahmedabad' or 'ahmedabad':
    print("The monument of Ahmedabad is => Lal Darvaja")
elif City=='Surat' or 'surat':
    print("The monument of Surat is => Dumas")
elif City=='Mumbai' or 'mumbai':
    print("The monument of Mumbai is => Bombay Gate")
else:
    print("Enter the city from Ahmedabad, Mumbai or Surat")